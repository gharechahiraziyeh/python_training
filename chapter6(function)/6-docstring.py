def max3(x, y, z):
    """Receives three number as input and return the largest of them

    parameters:
        x (int): A decimal integer
        y (int): Another decimal integer
        z (int): Another decimal integer

        Returns:
            max_int (int): largest of three numbers
        """
    return max(x, y, z)

